create view v_location_bien as
SELECT
    LOC.ID_LOCATION,
    LOC.BIEN AS ID_BIEN,
    LOC.CLIENT,
    LOC.DATE_DEBUT,
    LOC.DATE_FIN,
    V_BIEN.NOM AS BIEN_NOM,
    V_BIEN.DESCRIPTION AS BIEN_DESCRIPRION,
    V_BIEN.PROPRIETAIRE,
    V_BIEN.ID_TYPE_BIEN,
    V_BIEN.ID_BIEN_LOYER,
    V_BIEN.LOYER,
    V_BIEN.ID_TYPE_BIEN_COMMISSION,
    V_BIEN.COMMISSION,
    V_BIEN.NOM_TYPE_BIEN
FROM
    LOCATION AS LOC
    JOIN V_BIEN ON V_BIEN.ID_BIEN = LOC.BIEN;